// val/var identifier : type = initialization
// this is how it's written
fun main() {
    var name: String = "Damarazka"
    name = "Damarazka Akhyari"

    val firstVar = "Dimarazka"
    val secondVar = "Tsabita"

    val numb1 = 10
    val numb2 = 12

// val address: String = "Salatiga"
// address = "Semarang"
// value can't change

    println(name)
// println(address)
    println(firstVar + secondVar)
    println(numb1 + numb2)
}